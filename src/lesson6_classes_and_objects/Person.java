package lesson6_classes_and_objects;


//    Любую программу в java  можно можно представить как набор взаимодействующих между собой объектов.
//    Класс - это шаблон или описание объекта, а объект - это экземпляр класса.

//    Любой объект может обладать двумя основными характеристиками:
//    - состояние - некоторые данные, которые хранитт объект
//    - поведение - действия, которые может совершать объект

//   Переменные (или по другому поля) класса - применяются для хранения состояния объекта.
//   Методы - применяются для определения поведения класса.

//   Новый класс - это как новый тип переменной (например int, String и т.д.)


// класс обределяется с помощью ключевого слова class
class Person {
    //    между фигурными скобками помещается тело класса, к которому относятся поля (свойства) и методы этого класса
    String name; //поля класса - имя
    int age; // поле класса - возраст

// Методы класса, структура метода:
//  [модификатор доступа] [спецификатор] Тип_возвращаемого_значения  Имя_метода  (Параметры){
//    тело метода
//   }

// 1. Модификаторы доступа (кто может вызвать?): они определяют "видимость" метода для других классов.
// public — метод виден всем (самый частый вариант).
// private — метод виден только внутри этого же класса (нужен для "внутренней кухни").
// protected — виден внутри пакета и в наследниках.
// по умолчанию (default) — если ничего не писать, метод виден только внутри текущего пакета.

// 2. Спецификаторы (особые свойства): иногда перед типом возврата стоят дополнительные слова
// static — метод принадлежит самому классу, а не конкретному объекту (как Math.sqrt()).
// final — метод нельзя переопределить у наследников.

// 3. Тип возвращаемого значения (Что получим в итоге?): метод обязан сказать, что он вернет после работы.
// void — "пустота". Метод просто выполняет работу (как наш displayInfo) и ничего не отдает.
// Примитивы (int, double, boolean...) — метод вернет число или логическое значение.
// Объекты (String, Person, Car) — метод вернет ссылку на объект.
// Важно: Если тип не void, в теле метода обязательно должно быть ключевое слово return.


// 4. Параметры (Что нужно методу для работы?):
// Это переменные, которые метод ждет «на вход». Их может не быть вовсе (), а может быть много.

    // 1. Твой пример: void (ничего не возвращает), без параметров
    public void displayInfo() {

        System.out.printf("Name: %s \tAge: %d\n", name, age);
    }

    // 2. С параметром: void, принимает новое имя
    public void changeName(String newName) {

        name = newName;
    }

    // 3. С возвращаемым значением: возвращает int (год рождения)
    // Допустим, сейчас 2026 год
    public int calculateBirthYear() {
        int birthYear = 2026 - age;
        return birthYear; // Обязательно возвращаем результат
    }

    // 4. С несколькими параметрами и возвратом String
    public String getGreeting(String dayTime, boolean isFormal) {
        if (isFormal) {
            return "Good " + dayTime + ", Mr/Ms " + name;
        } else {
            return "Hi, " + name + "! Have a nice " + dayTime;
        }
    }

    //    Кроме обычных методов класс могут определять специальные методы, которые называются КОНСТРУКТОРАМИ.
//    Конструкторы вызываются при создании нового объекта данного класса. Другим словами конутрукторы выполняют инициализацию объекта. Его единственная задача — правильно создать объект.
//    1. Конструктор по умолчанию: если мы не написали ни одного конструктора, Java сама невидимо добавляет «пустой» конструктор:
    public Person() {
    } // Конструктор по умолчанию

    //    Но внимание! Как только мы напишем свой конструктор (например, с параметрами), «пустой» конструктор исчезнет. Если он нам всё еще нужен, его придется прописать вручную.
//    2. Несколько конструкторов: в одном классе может быть несколько конструкторов. Это как в автосалоне: можно купить машину в «базовой» комплектации, а можно с «полным фаршем».
// Конструктор №1: знаем всё
    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Конструктор №2: знаем только имя (возраст будет 0 по умолчанию)
    public Person(String name) {
        this.name = name;
        this.age = 0;
    }

//    Итого: у нас сейчас в классе 3 конструктора

//    КЛЮЧЕВОЕ СЛОВО THIS, разберём на примере:
//public Person(String name, int age) {
//    this.name = name; // "Запиши в поле ЭТОГО объекта имя из параметра"
//    this.age = age;   // "Запиши в поле ЭТОГО объекта возраст из параметра"
//    }
//    Зачем тут this? > У нас и поле называется name, и параметр в скобках называется name. Чтобы Java не запуталась, мы говорим: this.name — это переменная «хозяина» (поля класса), а просто name — это то, что прилетело снаружи.

}


